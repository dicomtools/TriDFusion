                           'MenuBar', 'none',...
                           'Color', viewerBackgroundColor('get'), ...
                           'Name', 'FDHT Machine Learning SUV Segmentation',...
                           'Toolbar','none'...               
                           );    
            end

            setObjectIcon(dlgMachineLearningFDHT);

            % Exclude organs from AI analysis
    
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'text',...
                      'FontWeight','bold'  ,...
                      'string'  , 'Exclude organs from AI analysis',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'position', [20 315 300 20]...
                      );
    
        % Brain
    
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'text',...
                      'Enable'  , 'Inactive',...
                      'string'  , 'Brain',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'ButtonDownFcn'  , @chkFDHTBrainCallback, ...
                      'position', [60 290 150 20]...
                      );
    
        chkFDHTBrain = ...
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'checkbox',...
                      'enable'  , 'on',...
                      'value'   , excludeFDHTBrain('get'),...
                      'position', [40 290 20 20],...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'Callback', @chkFDHTBrainCallback...
                      );
    
        % Spleen
    
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'text',...
                      'Enable'  , 'Inactive',...
                      'string'  , 'Spleen',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'ButtonDownFcn'  , @chkFDHTSpleenCallback, ...
                      'position', [60 265 150 20]...
                      );
    
        chkFDHTSpleen = ...
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'checkbox',...
                      'enable'  , 'on',...
                      'value'   , excludeFDHTSpleen('get'),...
                      'position', [40 265 20 20],...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'Callback', @chkFDHTSpleenCallback...
                      );
    
        % Kidney left
    
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'text',...
                      'Enable'  , 'Inactive',...
                      'string'  , 'Kidney left',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'ButtonDownFcn'  , @chkFDHTKidneyLeftCallback, ...
                      'position', [60 240 150 20]...
                      );
    
        chkFDHTKidneyLeft = ...
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'checkbox',...
                      'enable'  , 'on',...
                      'value'   , excludeFDHTKidneyLeft('get'),...
                      'position', [40 240 20 20],...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'Callback', @chkFDHTKidneyLeftCallback...
                      );
    
        % Kidney right
    
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'text',...
                      'Enable'  , 'Inactive',...
                      'string'  , 'Kidney right',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'ButtonDownFcn'  , @chkFDHTKidneyRightCallback, ...
                      'position', [60 215 150 20]...
                      );
    
        chkFDHTKidneyRight = ...
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'checkbox',...
                      'enable'  , 'on',...
                      'value'   , excludeFDHTKidneyRight('get'),...
                      'position', [40 215 20 20],...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'Callback', @chkFDHTKidneyRightCallback...
                      );
        
        % Small bowel
    
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'text',...
                      'Enable'  , 'Inactive',...
                      'string'  , 'Small bowel',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'ButtonDownFcn'  , @chkFDHTSmallBowelCallback, ...
                      'position', [60 190 150 20]...
                      );
    
        chkFDHTSmallBowel = ...
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'checkbox',...
                      'enable'  , 'on',...
                      'value'   , excludeFDHTSmallBowel('get'),...
                      'position', [40 190 20 20],...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'Callback', @chkFDHTSmallBowelCallback...
                      );
    
        % Urinary bladder
    
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'text',...
                      'Enable'  , 'Inactive',...
                      'string'  , 'Urinary bladder',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'ButtonDownFcn'  , @chkFDHTUrinaryBladderCallback, ...
                      'position', [60 165 150 20]...
                      );
    
        chkFDHTUrinaryBladder = ...
            uicontrol(dlgMachineLearningFDHT,...
                      'style'   , 'checkbox',...
                      'enable'  , 'on',...
                      'value'   , excludeFDHTUrinaryBladder('get'),...
                      'position', [40 165 20 20],...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'Callback', @chkFDHTUrinaryBladderCallback...
                      );
    
        % Options
    
            uicontrol(dlgMachineLearningFDHT,...
                      'style'     , 'text',...
                      'FontWeight','bold'  ,...
                      'string'    , 'Options',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'position', [20 115 300 20]...
                      );    
    
             % Pixel Edge
        
                uicontrol(dlgMachineLearningFDHT,...
                          'style'   , 'text',...
                          'Enable'  , 'Inactive',...
                          'string'  , 'Pixel Edge',...
                          'horizontalalignment', 'left',...
                          'BackgroundColor', viewerBackgroundColor('get'), ...
                          'ForegroundColor', viewerForegroundColor('get'), ...                   
                          'ButtonDownFcn'  , @chkFDHTPixelEdgeCallback, ...
                          'position', [40 90 150 20]...
                          );
        
