            % Exclude List
    
            % Other Organs
    
            tFDHT.exclude.organ.brain       = excludeFDHTBrain('get');
            tFDHT.exclude.organ.kidneyLeft  = excludeFDHTKidneyLeft('get');
            tFDHT.exclude.organ.kidneyRight = excludeFDHTKidneyRight('get');
            tFDHT.exclude.organ.spleen      = excludeFDHTSpleen('get');
    
            % Gastrointestinal Tract Name
    
            tFDHT.exclude.gastrointestinal.smallBowel     = excludeFDHTSmallBowel('get');
            tFDHT.exclude.gastrointestinal.urinaryBladder = excludeFDHTUrinaryBladder('get');
    
            % Options
    
            tFDHT.options.smalestVoiValue = FDHTSmalestVoiValue('get');
            tFDHT.options.pixelEdge       = pixelEdge('get');
    
            setMachineLearningFDHT(sSegmentatorScript, tFDHT);     
        end
    end

    % Exclude organs from AI analysis

    % Brain

    function chkFDHTBrainCallback(hObject, ~)  
                
        bObjectValue = get(chkFDHTBrain, 'Value');
        
        if strcmpi(get(hObject, 'Style'), 'text')
            
            set(chkFDHTBrain, 'Value', ~bObjectValue);
        end        
        
        bObjectValue = get(chkFDHTBrain, 'Value');

        excludeFDHTBrain('set', bObjectValue);

    end

    % Kidney Left

    function chkFDHTKidneyLeftCallback(hObject, ~)  
                
        bObjectValue = get(chkFDHTKidneyLeft, 'Value');
        
        if strcmpi(get(hObject, 'Style'), 'text')
            
            set(chkFDHTKidneyLeft, 'Value', ~bObjectValue);
        end        
        
        bObjectValue = get(chkFDHTKidneyLeft, 'Value');

        excludeFDHTKidneyLeft('set', bObjectValue);

    end

    % Kidney Right

    function chkFDHTKidneyRightCallback(hObject, ~)  
                
        bObjectValue = get(chkFDHTKidneyRight, 'Value');
        
        if strcmpi(get(hObject, 'Style'), 'text')
            
            set(chkFDHTKidneyRight, 'Value', ~bObjectValue);
        end        
        
        bObjectValue = get(chkFDHTKidneyRight, 'Value');

        excludeFDHTKidneyRight('set', bObjectValue);

    end

    % Small Bowel

    function chkFDHTSmallBowelCallback(hObject, ~)  
                
        bObjectValue = get(chkFDHTSmallBowel, 'Value');
        
        if strcmpi(get(hObject, 'Style'), 'text')
            
            set(chkFDHTSmallBowel, 'Value', ~bObjectValue);
        end        
        
        bObjectValue = get(chkFDHTSmallBowel, 'Value');

        excludeFDHTSmallBowel('set', bObjectValue);

    end


    % Spleen

    function chkFDHTSpleenCallback(hObject, ~)  
                
        bObjectValue = get(chkFDHTSpleen, 'Value');
        
        if strcmpi(get(hObject, 'Style'), 'text')
            
            set(chkFDHTSpleen, 'Value', ~bObjectValue);
        end        
        
        bObjectValue = get(chkFDHTSpleen, 'Value');

        excludeFDHTSpleen('set', bObjectValue);

    end

    function edtFDHTSmalestVoiValueCallback(~, ~)

        dObjectValue = str2double(get(edtFDHTSmalestVoiValue, 'String'));

        if dObjectValue < 0

            dObjectValue = 0;

            set(edtFDHTSmalestVoiValue, 'String', num2str(dObjectValue));
        end

        FDHTSmalestVoiValue('set', dObjectValue);

    end

    function chkFDHTPixelEdgeCallback(hObject, ~)  
                
        bObjectValue = get(chkFDHTPixelEdge, 'Value');
        
        if strcmpi(get(hObject, 'Style'), 'text')
            
            set(chkFDHTPixelEdge, 'Value', ~bObjectValue);
        end        
        
        bObjectValue = get(chkFDHTPixelEdge, 'Value');

        pixelEdge('set', bObjectValue);
        
        % Set contour panel checkbox

        set(chkPixelEdgePtr('get'), 'Value', pixelEdge('get'));
    end


    function cancelMachineLearningFDHTCallback(~, ~)   

        delete(dlgMachineLearningFDHT);
    end
    
    function proceedMachineLearningFDHTCallback(~, ~)

        % Exclude List

        % Other Organs

        tFDHT.exclude.organ.brain       = get(chkFDHTBrain      , 'value');
        tFDHT.exclude.organ.kidneyLeft  = get(chkFDHTKidneyLeft , 'value');
        tFDHT.exclude.organ.kidneyRight = get(chkFDHTKidneyRight, 'value');
        tFDHT.exclude.organ.spleen      = get(chkFDHTSpleen, 'value');

        % Gastrointestinal Tract Name

        tFDHT.exclude.gastrointestinal.smallBowel     = get(chkFDHTSmallBowel    , 'value');
        tFDHT.exclude.gastrointestinal.urinaryBladder = get(chkFDHTUrinaryBladder, 'value');

        % Options

        tFDHT.options.smalestVoiValue = str2double(get(edtFDHTSmalestVoiValue    , 'String'));
        tFDHT.options.pixelEdge       = get(chkFDHTPixelEdge                     , 'value');

        delete(dlgMachineLearningFDHT);

        setMachineLearningFDHT(sSegmentatorScript, tFDHT); 
    end
    
end