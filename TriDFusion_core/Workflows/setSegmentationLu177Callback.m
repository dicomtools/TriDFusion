        % Bone mask threshold
    
            uicontrol(dlgLu177Segmentation,...
                      'style'   , 'text',...
                      'Enable'  , 'On',...
                      'string'  , 'Bone mask threshold (HU)',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'position', [20 112 250 20]...
                      );
    
        edtLu177BoneMaskThresholdValue = ...
            uicontrol(dlgLu177Segmentation, ...
                      'Style'   , 'Edit', ...
                      'Position', [285 115 75 20], ...
                      'String'  , num2str(Lu177SegmentationBoneMaskThresholdValue('get')), ...
                      'Enable'  , 'on', ...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...
                      'CallBack', @edtLu177BoneMaskThresholdValueCallback ...
                      ); 
    
         % Smallest Contour (ml)
    
            uicontrol(dlgLu177Segmentation,...
                      'style'   , 'text',...
                      'Enable'  , 'On',...
                      'string'  , 'Smallest Contour (ml)',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'position', [20 90 250 20]...
                      );
    
        edtLu177SmalestVoiValue = ...
            uicontrol(dlgLu177Segmentation, ...
                      'Style'   , 'Edit', ...
                      'Position', [285 90 75 20], ...
                      'String'  , num2str(Lu177SmalestVoiValue('get')), ...
                      'Enable'  , 'on', ...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...
                      'CallBack', @edtLu177SmalestVoiValueCallback ...
                      );        
    
        % Pixel Edge
    
            uicontrol(dlgLu177Segmentation,...
                      'style'   , 'text',...
                      'Enable'  , 'Inactive',...
                      'string'  , 'Pixel Edge',...
                      'horizontalalignment', 'left',...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'ButtonDownFcn'  , @chkLu177PixelEdgeCallback, ...
                      'position', [40 65 150 20]...
                      );
    
        chkLu177PixelEdge = ...
            uicontrol(dlgLu177Segmentation,...
                      'style'   , 'checkbox',...
                      'enable'  , 'on',...
                      'value'   , pixelEdge('get'),...
                      'position', [20 65 20 20],...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...                   
                      'Callback', @chkLu177PixelEdgeCallback...
                      );
    
         % Cancel or Proceed
    
         uicontrol(dlgLu177Segmentation,...
                   'String','Cancel',...
                   'Position',[285 7 75 25],...
                   'BackgroundColor', viewerBackgroundColor('get'), ...
                   'ForegroundColor', viewerForegroundColor('get'), ...                
                   'Callback', @cancelLu177SegmentationCallback...
                   );
    
         uicontrol(dlgLu177Segmentation,...
                  'String','Continue',...
                  'Position',[200 7 75 25],...
                  'BackgroundColor', viewerBackgroundColor('get'), ...
                  'ForegroundColor', viewerForegroundColor('get'), ...               
                  'Callback', @proceedLu177SegmentationCallback...
                  );    

    else % Execute the segmentation using the default values

        setSegmentationLu177(Lu177SegmentationBoneMaskThresholdValue('get'), ...
                             Lu177SmalestVoiValue('get'), ...
                             pixelEdge('get'), ...
                             true); 
    end

    function edtLu177SmalestVoiValueCallback(~, ~)

        dObjectValue = str2double(get(edtLu177SmalestVoiValue, 'String'));

        if dObjectValue < 0

            dObjectValue = 0;

            set(edtLu177SmalestVoiValue, 'String', num2str(dObjectValue));
        end

        Lu177SmalestVoiValue('set', dObjectValue);

    end

    function chkLu177PixelEdgeCallback(hObject, ~)  
                
        bObjectValue = get(chkLu177PixelEdge, 'Value');
        
        if strcmpi(get(hObject, 'Style'), 'text')
            
            set(chkLu177PixelEdge, 'Value', ~bObjectValue);
        end        
        
        bObjectValue = get(chkLu177PixelEdge, 'Value');

        pixelEdge('set', bObjectValue);
        
        % Set contour panel checkbox
        set(chkPixelEdgePtr('get'), 'Value', pixelEdge('get'));
    end

%     function edtLu177BoundaryPercentOfMaxValueCallback(~, ~)     
% 
%         dBoundaryPercent = str2double(get(edtLu177BoundaryPercentOfMaxValue, 'String'));
% 
%         if dBoundaryPercent <= 0
% 
%             dBoundaryPercent = 10;
%             set(edtLu177BoundaryPercentOfMaxValue, 'String', num2str(dBoundaryPercent));
% 
%         elseif dBoundaryPercent >= 100
%             
%             dBoundaryPercent = 10;
%             set(edtLu177BoundaryPercentOfMaxValue, 'String', num2str(dBoundaryPercent));           
%         end
% 
%         Lu177SegmentationBoundaryPercentValue('set', dBoundaryPercent);
% 
%     end

    function edtLu177BoneMaskThresholdValueCallback(~, ~)     

        dBoneMaskThreshold = str2double(get(edtLu177BoneMaskThresholdValue, 'String'));

        if dBoneMaskThreshold <= 0

            dBoneMaskThreshold = 100;
            set(edtLu177BoneMaskThresholdValue, 'String', num2str(dBoneMaskThreshold));        
        end

        Lu177SegmentationBoneMaskThresholdValue('set', dBoneMaskThreshold);

    end

    function cancelLu177SegmentationCallback(~, ~)   

        delete(dlgLu177Segmentation);
    end
    
    function proceedLu177SegmentationCallback(~, ~)

        dSmalestVoiValue   = str2double(get(edtLu177SmalestVoiValue, 'String'));
        dPixelEdge         = get(chkLu177PixelEdge, 'value');
%         dBoundaryPercent   = str2double(get(edtLu177BoundaryPercentOfMaxValue, 'String'))/100;
        dBoneMaskThreshold = str2double(get(edtLu177BoneMaskThresholdValue, 'String'));

        delete(dlgLu177Segmentation);

        setSegmentationLu177(dBoneMaskThreshold, dSmalestVoiValue, dPixelEdge, false); 
    end

end