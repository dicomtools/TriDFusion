    if exist('hObject', 'var')

        DLG_Lu177_PERCENT_X = 380;
        DLG_Lu177_PERCENT_Y = 160;

        if viewerUIFigure('get') == true
    
            dlgLu177Segmentation = ...
                uifigure('Position', [(getMainWindowPosition('xpos')+(getMainWindowSize('xsize')/2)-DLG_Lu177_PERCENT_X/2) ...
                                    (getMainWindowPosition('ypos')+(getMainWindowSize('ysize')/2)-DLG_Lu177_PERCENT_Y/2) ...
                                    DLG_Lu177_PERCENT_X ...
                                    DLG_Lu177_PERCENT_Y ...
                                    ],...
                       'Resize', 'off', ...
                       'Color', viewerBackgroundColor('get'),...
                       'WindowStyle', 'modal', ...
                       'Name' , 'PSMA - Lu177 Segmentation'...
                       );
        else       
            dlgLu177Segmentation = ...
                dialog('Position', [(getMainWindowPosition('xpos')+(getMainWindowSize('xsize')/2)-DLG_Lu177_PERCENT_X/2) ...
                                    (getMainWindowPosition('ypos')+(getMainWindowSize('ysize')/2)-DLG_Lu177_PERCENT_Y/2) ...
                                    DLG_Lu177_PERCENT_X ...
                                    DLG_Lu177_PERCENT_Y ...
                                    ],...
                       'MenuBar', 'none',...
                       'Resize', 'off', ...    
                       'NumberTitle','off',...
                       'MenuBar', 'none',...
                       'Color', viewerBackgroundColor('get'), ...
                       'Name', 'PSMA - Lu177 Segmentation',...
                       'Toolbar','none'...               
                       );
        end

        setObjectIcon(dlgLu177Segmentation);

    %     % Boundary percent of max
    % 
    %         uicontrol(dlgLu177Segmentation,...
    %                   'style'   , 'text',...
    %                   'Enable'  , 'On',...
    %                   'string'  , 'Boundary percent of max',...
    %                   'horizontalalignment', 'left',...
    %                   'BackgroundColor', viewerBackgroundColor('get'), ...
    %                   'ForegroundColor', viewerForegroundColor('get'), ...                   
    %                   'position', [20 162 250 20]...
    %                   );
    % 
    %     edtLu177BoundaryPercentOfMaxValue = ...
    %         uicontrol(dlgLu177Segmentation, ...
    %                   'Style'   , 'Edit', ...
    %                   'Position', [285 165 75 20], ...
    %                   'String'  , num2str(Lu177SegmentationBoundaryPercentValue('get')), ...
    %                   'Enable'  , 'on', ...
