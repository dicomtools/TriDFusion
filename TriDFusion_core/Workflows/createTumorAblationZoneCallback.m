function createTumorAblationZoneCallback(hObject, ~)
%function createTumorAblationZoneCallback(hObject, ~)
%Add a Marginal VOI Around an Existing VOI, the function is called from the main menu.
%See TriDFuison.doc (or pdf) for more information about options.
%
