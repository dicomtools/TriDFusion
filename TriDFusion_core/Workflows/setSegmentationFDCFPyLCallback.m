        if viewerUIFigure('get') == true
    
            dlgFDCFPyLSegmentation = ...
                uifigure('Position', [(getMainWindowPosition('xpos')+(getMainWindowSize('xsize')/2)-DLG_FDCFPyL_PERCENT_X/2) ...
