                            pixelEdge('get'), ...
                            true); 

    end

    function edtFDCFPyLSmalestVoiValueCallback(~, ~)

        dObjectValue = str2double(get(edtFDCFPyLSmalestVoiValue, 'String'));

        if dObjectValue < 0

            dObjectValue = 0;

            set(edtFDCFPyLSmalestVoiValue, 'String', num2str(dObjectValue));
        end

        PSMASmalestVoiValue('set', dObjectValue);

    end

    function chkFDCFPyLPixelEdgeCallback(hObject, ~)  
                
        bObjectValue = get(chkFDCFPyLPixelEdge, 'Value');
        
        if strcmpi(get(hObject, 'Style'), 'text')
            
            set(chkFDCFPyLPixelEdge, 'Value', ~bObjectValue);
        end        
        
        bObjectValue = get(chkFDCFPyLPixelEdge, 'Value');

        pixelEdge('set', bObjectValue);
        
        % Set contour panel checkbox
        set(chkPixelEdgePtr('get'), 'Value', pixelEdge('get'));
    end

