    function edtFDCFPyLBoneMaskThresholdValueCallback(~, ~)     

        dBoneMaskThreshold = str2double(get(edtFDCFPyLBoneMaskThresholdValue, 'String'));

        if dBoneMaskThreshold <= 0

            dBoneMaskThreshold = 100;
            set(edtFDCFPyLBoneMaskThresholdValue, 'String', num2str(dBoneMaskThreshold));        
        end

        PSMASegmentationBoneMaskThresholdValue('set', dBoneMaskThreshold);

    end

    function cancelFDCFPyLSegmentationCallback(~, ~)   

        delete(dlgFDCFPyLSegmentation);
    end
    
    function proceedFDCFPyLSegmentationCallback(~, ~)

        dSmalestVoiValue   = str2double(get(edtFDCFPyLSmalestVoiValue, 'String'));
        dPixelEdge         = get(chkFDCFPyLPixelEdge, 'value');
%         dBoundaryPercent   = str2double(get(edtFDCFPyLBoundaryPercentOfMaxValue, 'String'))/100;
        dBoneMaskThreshold = str2double(get(edtFDCFPyLBoneMaskThresholdValue, 'String'));

        delete(dlgFDCFPyLSegmentation);

        setSegmentationPSMA(dBoneMaskThreshold, dSmalestVoiValue, dPixelEdge, false); 
    end

