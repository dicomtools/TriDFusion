                      'position', [40 65 150 20]...
                      );
    
        chkFDCFPyLPixelEdge = ...
            uicontrol(dlgFDCFPyLSegmentation,...
                      'style'   , 'checkbox',...
                      'enable'  , 'on',...
                      'value'   , pixelEdge('get'),...
