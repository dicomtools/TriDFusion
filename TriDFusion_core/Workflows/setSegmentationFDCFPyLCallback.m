                       'Toolbar','none'...               
                       );
        end

        setObjectIcon(dlgFDCFPyLSegmentation);

    %     % Boundary percent of max
    % 
    %         uicontrol(dlgFDCFPyLSegmentation,...
    %                   'style'   , 'text',...
    %                   'Enable'  , 'On',...
    %                   'string'  , 'Boundary percent of max',...
