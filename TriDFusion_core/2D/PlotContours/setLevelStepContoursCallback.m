    if isPlotContours('get') == true 
        
        if size(dicomBuffer('get', [], get(uiSeriesPtr('get'), 'Value')), 3) == 1 % 2D Image

            imAxeFc = imAxeFcPtr ('get', [], get(uiFusedSeriesPtr('get'), 'Value')); 
            if ~isempty(imAxeFc)
                aLevelStep = get(imAxeFc, 'LevelStep');
                sLevelStep =  num2str(aLevelStep);
            else
                sLevelStep = '';
            end 

            if viewerUIFigure('get') == true
        
                dlgLevelStep = ...
                    uifigure('Position', [(getMainWindowPosition('xpos')+(getMainWindowSize('xsize')/2)-380/2) ...
                                    (getMainWindowPosition('ypos')+(getMainWindowSize('ysize')/2)-125/2) ...
                                    380 ...
                                    125 ...
                                    ],...
                           'Resize'     , 'off', ...
                           'Color'      , viewerBackgroundColor('get'),...
                           'WindowStyle', 'modal', ...
                           'Name'       , 'Plot Contours Level Step'...
                           );
            else

                dlgLevelStep = ...
                    dialog('Position', [(getMainWindowPosition('xpos')+(getMainWindowSize('xsize')/2)-380/2) ...
                                    (getMainWindowPosition('ypos')+(getMainWindowSize('ysize')/2)-125/2) ...
                                    380 ...
                                    125 ...
                                    ],...
                          'Color', viewerBackgroundColor('get'), ...
                          'Name', 'Plot Contours Level Step'...
                           ); 
            end

            setObjectIcon(dlgLevelStep);

            aDlgPosition = get(dlgLevelStep, 'Position');

            axeLevelStep = ...       
                axes(dlgLevelStep, ...
                     'Units'   , 'pixels', ...
                     'Position', [0 0 aDlgPosition(3) aDlgPosition(4)], ...
                     'Color'   , viewerBackgroundColor('get'),...
                     'XColor'  , viewerForegroundColor('get'),...
                     'YColor'  , viewerForegroundColor('get'),...
                     'ZColor'  , viewerForegroundColor('get'),...             
                     'Visible' , 'off'...             
                     );  
             axeLevelStep.Interactions = [];
             deleteAxesToolbar(axeLevelStep);
             disableDefaultInteractivity(axeLevelStep);

                 uicontrol(dlgLevelStep,...
                          'style'   , 'text',...
                          'string'  , 'Axe:',...
                          'horizontalalignment', 'left',...
                          'BackgroundColor', viewerBackgroundColor('get'), ...
                          'ForegroundColor', viewerForegroundColor('get'), ...                   
                          'position', [20 67 75 20]...
                          );
                           
          edtAxeLevelStep = ...
              uicontrol(dlgLevelStep,...
                        'style'     , 'edit',...
                        'Background', 'white',...
                        'string'    , sLevelStep,...
                        'BackgroundColor', viewerBackgroundColor('get'), ...
                        'ForegroundColor', viewerForegroundColor('get'), ...                 
                        'position'  , [95 70 265 20]...
                        ); 
                    
             % Cancel or Proceed

             uicontrol(dlgLevelStep,...
                       'String','Cancel',...
                       'Position',[285 7 75 25],...
                       'BackgroundColor', viewerBackgroundColor('get'), ...
                       'ForegroundColor', viewerForegroundColor('get'), ...                
                       'Callback', @cancelLevelStepCallback...
                       );

             uicontrol(dlgLevelStep,...
                      'String','Proceed',...
                      'Position',[200 7 75 25],...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...               
                      'Callback', @proceedLevelStepCallback...
                      );                    
                   
        else            
            imCoronalFc  = imCoronalFcPtr ('get', [], get(uiFusedSeriesPtr('get'), 'Value')); 
            imSagittalFc = imSagittalFcPtr('get', [], get(uiFusedSeriesPtr('get'), 'Value')); 
            imAxialFc    = imAxialFcPtr   ('get', [], get(uiFusedSeriesPtr('get'), 'Value')); 
            if link2DMip('get') == true
                imMipFc = imMipFcPtr('get', [], get(uiFusedSeriesPtr('get'), 'Value'));   
            end  

            if ~isempty(imCoronalFc)
                aCoronalLevelStep = get(imCoronalFc, 'LevelStep');
                sCoronalLevelStep = num2str(aCoronalLevelStep);
            else
                sCoronalLevelStep = '';
            end  
            
            if ~isempty(imSagittalFc)
                aSagittalLevelStep = get(imSagittalFc, 'LevelStep');
                sSagittalLevelStep = num2str(aSagittalLevelStep);
            else
                sSagittalLevelStep = '';                
            end  
                
            if ~isempty(imAxialFc)
                aAxialLevelStep = get(imAxialFc, 'LevelStep');
                sAxialLevelStep = num2str(aAxialLevelStep);
            else
                sAxialLevelStep = '';                
            end  
            
            if link2DMip('get') == true
                if ~isempty(imMipFc)
                    aMipLevelStep = get(imMipFc, 'LevelStep');
                    sMipLevelStep = num2str(aMipLevelStep);
                else
                    sMipLevelStep = '';                
                end                  
            else 
                sMipLevelStep = '';                
            end 

            if viewerUIFigure('get') == true
        
                dlgLevelStep = ...
                    uifigure('Position', [(getMainWindowPosition('xpos')+(getMainWindowSize('xsize')/2)-380/2) ...
                                    (getMainWindowPosition('ypos')+(getMainWindowSize('ysize')/2)-195/2) ...
                                    380 ...
                                    195 ...
                                    ],...
                           'Resize'     , 'off', ...
                           'Color'      , viewerBackgroundColor('get'),...
                           'WindowStyle', 'modal', ...
                           'Name'       , 'Plot Contours Level Step'...
                           );
            else

                dlgLevelStep = ...
                    dialog('Position', [(getMainWindowPosition('xpos')+(getMainWindowSize('xsize')/2)-380/2) ...
                                    (getMainWindowPosition('ypos')+(getMainWindowSize('ysize')/2)-195/2) ...
                                    380 ...
                                    195 ...
                                    ],...
                          'Color', viewerBackgroundColor('get'), ...
                          'Name', 'Plot Contours Level Step'...
                           ); 
            end

            setObjectIcon(dlgLevelStep);

            aDlgPosition = get(dlgLevelStep, 'Position');

            axeLevelStep = ...       
                axes(dlgLevelStep, ...
                     'Units'   , 'pixels', ...
                     'Position', [0 0 aDlgPosition(3) aDlgPosition(4)], ...
                     'Color'   , viewerBackgroundColor('get'),...
                     'XColor'  , viewerForegroundColor('get'),...
                     'YColor'  , viewerForegroundColor('get'),...
                     'ZColor'  , viewerForegroundColor('get'),...             
                     'Visible' , 'off'...             
                     );  
             axeLevelStep.Interactions = [];
             deleteAxesToolbar(axeLevelStep);
             disableDefaultInteractivity(axeLevelStep);

                 uicontrol(dlgLevelStep,...
                          'style'   , 'text',...
                          'string'  , 'Coronal:',...
                          'horizontalalignment', 'left',...
                          'BackgroundColor', viewerBackgroundColor('get'), ...
                          'ForegroundColor', viewerForegroundColor('get'), ...                   
                          'position', [20 142 75 20]...
                          );
                      
          edtCoronalLevelStep = ...
              uicontrol(dlgLevelStep,...
                        'style'     , 'edit',...
                        'Background', 'white',...
                        'string'    , sCoronalLevelStep,...
                        'BackgroundColor', viewerBackgroundColor('get'), ...
                        'ForegroundColor', viewerForegroundColor('get'), ...                 
                        'position'  , [95 145 265 20]...
                        ); 
                    
                 uicontrol(dlgLevelStep,...
                          'style'   , 'text',...
                          'string'  , 'Sagittal:',...
                          'horizontalalignment', 'left',...
                          'BackgroundColor', viewerBackgroundColor('get'), ...
                          'ForegroundColor', viewerForegroundColor('get'), ...                   
                          'position', [20 117 75 20]...
                          );
                      
          edtSagittalLevelStep = ...
              uicontrol(dlgLevelStep,...
                        'style'     , 'edit',...
                        'Background', 'white',...
                        'string'    , sSagittalLevelStep,...
                        'BackgroundColor', viewerBackgroundColor('get'), ...
                        'ForegroundColor', viewerForegroundColor('get'), ...                 
                        'position'  , [95 120 265 20]...
                        ); 
                    
                 uicontrol(dlgLevelStep,...
                          'style'   , 'text',...
                          'string'  , 'Axial:',...
                          'horizontalalignment', 'left',...
                          'BackgroundColor', viewerBackgroundColor('get'), ...
                          'ForegroundColor', viewerForegroundColor('get'), ...                   
                          'position', [20 93 75 20]...
                          );
                      
          edtAxialLevelStep = ...
              uicontrol(dlgLevelStep,...
                        'style'     , 'edit',...
                        'Background', 'white',...
                        'string'    , sAxialLevelStep,...
                        'BackgroundColor', viewerBackgroundColor('get'), ...
                        'ForegroundColor', viewerForegroundColor('get'), ...                 
                        'position'  , [95 95 265 20]...
                        ); 
                    
                 uicontrol(dlgLevelStep,...
                          'style'   , 'text',...
                          'string'  , 'MIP:',...
                          'horizontalalignment', 'left',...
                          'BackgroundColor', viewerBackgroundColor('get'), ...
                          'ForegroundColor', viewerForegroundColor('get'), ...                   
                          'position', [20 67 75 20]...
                          );
                      
          if link2DMip('get') == true
              sMipStepEnable = 'on';
          else
              sMipStepEnable = 'off';
          end
                      
          edtMipLevelStep = ...
              uicontrol(dlgLevelStep,...
                        'style'     , 'edit',...
                        'Enable'    , sMipStepEnable, ...
                        'Background', 'white',...
                        'string'    , sMipLevelStep,...
                        'BackgroundColor', viewerBackgroundColor('get'), ...
                        'ForegroundColor', viewerForegroundColor('get'), ...                 
                        'position'  , [95 70 265 20]...
                        ); 
                    
             % Cancel or Proceed

             uicontrol(dlgLevelStep,...
                       'String','Cancel',...
                       'Position',[285 7 75 25],...
                       'BackgroundColor', viewerBackgroundColor('get'), ...
                       'ForegroundColor', viewerForegroundColor('get'), ...                
                       'Callback', @cancelLevelStepCallback...
                       );

             uicontrol(dlgLevelStep,...
                      'String','Proceed',...
                      'Position',[200 7 75 25],...
                      'BackgroundColor', viewerBackgroundColor('get'), ...
                      'ForegroundColor', viewerForegroundColor('get'), ...               
                      'Callback', @proceedLevelStepCallback...
                      );           

        end                              
    end
    
    function cancelLevelStepCallback(~, ~)
        
        delete(dlgLevelStep);
    end

    function proceedLevelStepCallback(~, ~) 
        
        if size(dicomBuffer('get'), 3) == 1 % 2D Image
            imAxeFc = imAxeFcPtr ('get', [], get(uiFusedSeriesPtr('get'), 'Value')); 
            if ~isempty(imAxeFc)
                set(imAxeFc, 'LevelStep',  str2num(edtAxeLevelStep.String));
            end 
        else            
            imCoronalFc  = imCoronalFcPtr ('get', [], get(uiFusedSeriesPtr('get'), 'Value')); 
            imSagittalFc = imSagittalFcPtr('get', [], get(uiFusedSeriesPtr('get'), 'Value')); 
            imAxialFc    = imAxialFcPtr   ('get', [], get(uiFusedSeriesPtr('get'), 'Value')); 
            if link2DMip('get') == true
                imMipFc = imMipFcPtr('get', [], get(uiFusedSeriesPtr('get'), 'Value'));   
            end            
            
            if ~isempty(imCoronalFc)
                set(imCoronalFc, 'LevelStep', str2num(edtCoronalLevelStep.String));
            end
            
            if ~isempty(imSagittalFc)
                set(imSagittalFc, 'LevelStep',  str2num(edtSagittalLevelStep.String));
            end

            if ~isempty(imAxialFc)
                set(imAxialFc, 'LevelStep',  str2num(edtAxialLevelStep.String));
            end

            if link2DMip('get') == true
                if ~isempty(imMipFc)
                    set(imMipFc, 'LevelStep',  str2num(edtMipLevelStep.String));
                end
            end            
        end 
        
        delete(dlgLevelStep);
        
    end
end



